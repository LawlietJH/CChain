def Hex_Ascii(Hex):
	#~ Hexadecimal a Ascii
	
	Hex = Hex.replace(" ", "")

	Ascii = ''.join((chr(int(Hex[i:i+2], 16)) for i in range(0, len(Hex), 2)))
	
	print("\n\n [+] Ascii: \n\n >> "+Ascii)


